#include <stdio.h> // Incluye la biblioteca estándar de entrada y salida

int main() { // Función principal
    int a = 15; // Declara una variable entera 'a' y le asigna el valor 10
    int b = 15; // Declara una variable entera 'b' y le asigna el valor 5
    int suma = a + b; // Declara una variable 'suma' y almacena la suma de 'a' y 'b'
    printf("La suma de %d y %d es %d\n", a, b, suma); // Imprime el resultado en pantalla
    return 0; // Indica que el programa terminó correctamente
}
